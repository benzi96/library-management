private int checkngay(String ngay,String thang,String nam,String doituong)
    {
        int check=0;
        try{
        int ngay1=Integer.parseInt(ngay);
        int thang1=Integer.parseInt(thang);
        int nam1=Integer.parseInt(nam);
        Calendar cal = Calendar.getInstance();
        if(doituong.equals("Nhân viên"))          
        if(nam1 <= (cal.get(Calendar.YEAR)-18))check=1;
        if(doituong.equals("Thành viên"))
        if(nam1 <= (cal.get(Calendar.YEAR)-10))check=1;
        if(doituong.equals("Đăng kí"))
        if(nam1 <= cal.get(Calendar.YEAR))check=1;
        if(check==1)
        {
            switch(thang1)
            {
                case 1: case 3: case 5: case 7: case 8: case 10: case 12:
                {
                    if(ngay1 <= 31 && ngay1 > 0)                    
                        check = 1;
                    else
                        check = 0;                    
                }break;
                case 4: case 6: case 9: case 11:
                    if(ngay1 <= 30 && ngay1 > 0)
                        check = 1;
                    else
                        check = 0;
                    break;
                case 2:
                     if( (nam1 % 400 == 0) || (nam1 % 4 == 0 && nam1 % 100 != 0) )
                     {
                         if(ngay1 <= 29 && ngay1 > 0)
                             check = 1;
                         else
                             check = 0;
                     }
                     else
                         if(ngay1 <=28 && ngay1 > 0)
                             check = 1;
                         else
                             check = 0;
                     break;
            }
        }} 
        catch(Exception e){
            check=0;
        }
        return check;
    }
